        <p align="justify"><font size="+1">Proses pengelolaan di puskesmas Kiangroke 
          masih menggunakan buku catatan besar. Prosesnya pasien yang datang langsung 
          menuju ke bagian pendaftaran sambil mengambil no urut antrian. Setelah 
          itu, pasien diminta menunjukan kartu identitas untuk dicatat oleh petugas 
          atau sekarang ini biasanya menggunakan kartu BPJS (Badan Penyelenggara 
          Jaminan Sosial) yang belum lama ini diusung oleh pemerintah. Jika pasien 
          yang datang dari kecamatan banjaran maka pasien tersebut tidak perlu 
          membayar biaya admin sedangkan jika pasien dari kecamatan yang berbeda 
          dikenakan biaya Rp. 4000,- untuk biaya administrasinya. Setelah itu 
          pasien akan memilih untuk diperiksa ke poli gigi, poli umum, atau poli 
          KB sesuai yang dibutuhkan pasien yang bersangkutan. Setelah itu pasien 
          menunggu dipanggil oleh dokter kemudian dokter memeriksa dan mendiagnosis 
          penyakit pasien. Apabila pemeriksaan sudah selesai dokter akan memberi 
          resep obat yang di tulis di kartu berobat lalu dokter mencatat data 
          pasien di buku kunjungan. Lalu pasien pergi ke bagian apotek dengan 
          membawa kartu pasien yang sudah diisi dengan resep obat dan diberikan 
          ke bagian apotek lalu bagian apotek akan memberikan obat yang dibutuhkan 
          oleh pasien seperti yang tertera di resep dokter.</font></p></td>
